public class Lab048 {
    public static void main(String[] args) {
        int a=10;
        System.out.println(++a);
        System.out.println(a);
        //Preincrement
        // ++ infront of a variable like a -->Pre
        //Do increment first and then print the value

        int b=20;

        System.out.println(b++);
        System.out.println(b);

        //Post Inccrement
        //++ after the variable like a means post increment
        //first print the value then do the increment
    }
}
